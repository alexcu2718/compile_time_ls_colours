///This is a lazily initialised HashMap of file extensions to their corresponding ANSI colour codes.
/// It is built once at runtime from the `LS_COLORS_CUSTOM` 
///the default (LS_COLOR) is used if the environment variable is not set. This is an optional feature to allow custom colours easily.
pub static LS_COLOURS_HASHMAP_RUNTIME: LazyLock<HashMap<&'static [u8], &'static [u8], BuildHasherDefault<DefaultHasher>>> = LazyLock::new(|| {
    let mut map = HashMap::with_capacity_and_hasher(LS_COLOURS_DATA.len(), BuildHasherDefault::new());
    for (key, value) in LS_COLOURS_DATA {
        map.insert(*key, *value);
    }
    map
});
